let a = 7;
let b = 5;

// Arithmetic Operators
console.log("a+b = ", a + b);
console.log("a-b = ", a - b);
console.log("a*b = ", a * b);
console.log("a/b = ", a / b);
console.log("a**b = ", a ** b);
console.log("a%b = ", a % b);

// Incremental Operator
console.log("a++", a++);
// Will come 7
console.log("a", a);
// Here comes 8

console.log("++a", ++a);
// Will become 9 immediately

// Decremental Operator
console.log("a--", a--);
//will show 9 as it is
console.log("a", a);
// Here shows 8

console.log("--a", --a);
// Shows 7 immediately
